package com.relative.chat.bot.ia.domain.messaging;

/**
 * Categorías de plantillas de WhatsApp según Meta API
 */
public enum TemplateCategory {
    /**
     * Plantillas de autenticación - para códigos OTP, verificación de identidad
     */
    AUTHENTICATION,
    
    /**
     * Plantillas de marketing - para promociones, anuncios, contenido comercial
     */
    MARKETING,
    
    /**
     * Plantillas de utilidad - para confirmaciones, notificaciones, actualizaciones de estado
     */
    UTILITY
}
